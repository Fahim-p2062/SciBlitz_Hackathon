export const findDatabaseMatches = (subTypeCodePrefix: string, condition?: string): DetectionDatabaseEntry[] => {
  return detectionDatabaseEntries.filter(entry => {
    const matchesCode = entry.sub_type_code.startsWith(subTypeCodePrefix) || entry.category_code === subTypeCodePrefix;
    if (!condition || condition === 'All') return matchesCode;
    return matchesCode && entry.condition_state.toLowerCase().includes(condition.toLowerCase());
  });
};

export const searchDatabaseEntries = (query: string): DetectionDatabaseEntry[] => {
  if (!query || !query.trim()) return detectionDatabaseEntries.slice(0, 15);
  const q = query.toLowerCase().trim();
  return detectionDatabaseEntries.filter(entry => 
    entry.object_id.toLowerCase().includes(q) ||
    entry.object_name_en.toLowerCase().includes(q) ||
    entry.object_name_bn.toLowerCase().includes(q) ||
    entry.sub_type_code.toLowerCase().includes(q) ||
    entry.material.toLowerCase().includes(q)
  );
};
